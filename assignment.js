//how to convert feet to mile

function feetToMile(feet){
    var mile=feet*0.000189394;
    return mile;
}

var mile=feetToMile(15);
console.log(mile);

//how to calculate total amount of wood

function woodCalculator(chair,table,bed){
    var wood=chair*1+table*3+bed*5;
    return wood;
}
var wood=woodCalculator(5,7,8);
console.log(wood);

